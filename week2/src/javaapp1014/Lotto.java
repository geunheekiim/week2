package javaapp1014;

public class Lotto {

	public static void main(String[] args) {
		//로또 번호 6개를 저장할 배열을 생성
		//로또 번호는 정수
		int [] lotto = new int [6];
		
		
		
		//lotto 배열에 저장된 데이터를 출력
		for(int temp:lotto) {
			System.out.printf("%5d", lotto[temp]);
		}

		
		
		
		
		

	}

}
